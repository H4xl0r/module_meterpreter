<b>Meterpreter</b> is an advanced, dynamically extensible payload that uses in-memory DLL injection stagers and is extended over the network at runtime. <br>
It communicates over the stager socket and provides a comprehensive client-side Ruby API. It features command history, tab completion, channels, and more. <br>
Metepreter was originally written by skape for Metasploit 2.x, common extensions were merged for 3.x and is currently undergoing an overhaul for Metasploit 3.3. <br>
The server portion is implemented in plain C and is now compiled with MSVC, making it somewhat portable. <br>
The client can be written in any language but Metasploit has a full-featured Ruby client API. <br>
